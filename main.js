// 定义一个异步函数 fetchData 用于获取并处理 customize.json 文件中的数据
const fetchData = () => {
  // 使用 fetch API 获取 customize.json 文件
  fetch("customize.json")
    .then(data => data.json()) // 将获取的数据转换为 JSON 格式
    .then(data => {
      // 获取数据对象的键数组
      dataArr = Object.keys(data)
      // 遍历数据对象的键数组
      dataArr.map(customData => {
        // 检查当前键对应的值是否不为空字符串
        if (data[customData] !== "") {
          // 如果当前键是 "imagePath"
          if (customData === "imagePath") {
            // 获取具有 data-node-name 属性包含 "imagePath" 的元素，并设置其 src 属性
            document
              .querySelector(`[data-node-name*="${customData}"]`)
              .setAttribute("src", data[customData])
          // 如果当前键是 "fonts"
          } else if (customData === "fonts") {
            // 遍历字体数组
            data[customData].forEach(font => {
              // 创建一个新的 <link> 元素
              const link = document.createElement('link')
              link.rel = 'stylesheet' // 设置 <link> 元素的 rel 属性为 'stylesheet'
              link.href = font.path // 设置 <link> 元素的 href 属性为字体的路径
              document.head.appendChild(link) // 将 <link> 元素添加到 <head> 中
              //设置body字体
              document.body.style.fontFamily = font.name
            })
          } else if (customData === "music") {
            audioUrl = data[customData]
            audio = new Audio(audioUrl)
            audio.preload = "auto"
          } else {
            document.querySelector(`[data-node-name*="${customData}"]`).innerText = data[customData]
          }
        }

        // Check if the iteration is over
        // Run amimation if so
        if (dataArr.length === dataArr.indexOf(customData) + 1) {
          document.querySelector("#startButton").addEventListener("click", () => {
            document.querySelector(".startSign").style.display = "none"
            animationTimeline()
          }
          )
          // animationTimeline()
        }
      })
    })
}

